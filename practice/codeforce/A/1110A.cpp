void solve() {
	int a, b;
	cin >> a >> b;
	int n = 0;
	for (int i = 0; i < b; i++) {
		int x;
		cin >> x;
		n = (n*a+x)%2;
	}
	cout << (n==1 ? "odd" : "even");
}

