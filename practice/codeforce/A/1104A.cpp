#include <bits/stdc++.h>

using namespace std;

#define ll long long

void solve() {
	int n;
	cin >> n;
	int x;
	for (int i = 9; i > 0; i--) {
		if (n % i == 0) {
			x = i;
			break;
		}
	}
	int ans = n / x;
	cout << ans << "\n";
	while(ans--) {
		cout << x << " ";
	}
}

int main() {
	ios::sync_with_stdio(0);
	cin.tie(0);

	int t = 1, i = 1;
	//cin >> t;
	while(t--) {
		//cout << "Case #" << i < ": ";
		solve();
		//i++;
	}
	return 0;
}
