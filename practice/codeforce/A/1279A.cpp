#include <bits/stdc++.h>

using namespace std;

#define ll long long

void solve() {
	vector<ll> v;
	for (int i = 0; i < 3; i++) {
		ll a;
		cin >> a;
		v.push_back(a);
	}
	sort(v.begin(), v.end());

	if (v[2] > v[1]+v[0]+1) {
		cout << "No\n";
	} else {
		cout << "Yes\n";
	}
}

int main() {
	ios::sync_with_stdio(0);
	cin.tie(0);

	int t = 1, i = 1;
	cin >> t;
	while(t--) {
		//cout << "Case #" << i << ": ";
		solve();
		//i++;
	}
	return 0;
}


