#define ll long long

void solve() {
	string s;
	cin >> s;
	if (s.length() == 1) {
		cout << (s[0] - '0')/2;
	} else {
		int sum = 0;
		for (char c : s) {
			sum += c-'0';
		}
		cout << sum << "\n";
	}
}

