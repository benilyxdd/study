#include <bits/stdc++.h>
using namespace std;

#define ll long long
int a, b;

void solve() {
	cin >> a >> b;
	a -= 2;
	b -= 2;
	ll ans = 1;
	ans *= (a%3 == 0 ? a/3 : a/3+1);
	ans *= (b%3 == 0 ? b/3 : b/3+1);
	cout << ans << "\n";
}

int main() {
	ios::sync_with_stdio(0);
	cin.tie(0);

	int t = 1, i = 1;
	cin >> t;
	while(t--) {
		//cout << "Case #" << i << ": ";
		solve();
		//i++;
	}
	return 0;
}


